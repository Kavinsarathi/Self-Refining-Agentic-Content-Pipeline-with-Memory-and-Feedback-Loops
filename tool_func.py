async def checks_session_status(State):
    if os.path.exists(session_status_path):
        with open(session_status_path,'r') as file:
            stats=file.read()
    else:
         with open(session_status_path,'w+') as file:
            stats=file.write(0)       
            
    if stats:
        return "agent_calling"
    else:
        return "manual_login"

async def manual_login(State):
    async with async_playwright() as p:
        browser = await p.chromium.launch(headless=False)
        context = await browser.new_context()
        page = await context.new_page()
        await page.goto(base_url)  
        try:
            await page.wait_for_url(target_url,timeout=40000)
        except Exception as e:
            raise Exception(e)    

        if page.url == target_url:
            with open(session_state_store_path,'w+') as file:
                json.dump(await context.storage_state(),file,indent=5)
                with open(session_status_path,'w') as file:
                    file.write('1')                  
        else:
            raise Exception("Please ensure that the first manual login has been successfully accomplished")       
           
        await browser.close()

async def session_login(State):
    async with async_playwright() as p:
        browser = await p.chromium.launch(headless=False)
        context = await browser.new_context(storage_state=session_state_store_path)
        page = await context.new_page()
        await page.goto(target_url)
        await page.get_by_role("button",name="Start a post").click()
        await page.get_by_role("textbox").fill(State["content"])
        await page.wait_for_timeout(10000)
        
