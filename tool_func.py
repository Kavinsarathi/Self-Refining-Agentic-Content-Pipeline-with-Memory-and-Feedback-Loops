async def Agent_calling(State):
    @tool
    async def create_content(TOPIC:str)->str:
        '''These tool used to create or generate the content based on the topic'''
        AUDIENCE = Audience
        TONE = tone
        LENGTH = Length
        CTA_TYPE = CTA_type
        PREVIOUS_POSTS_HISTORY=State['chat_history']
        param = {"TOPIC":TOPIC,"AUDIENCE":AUDIENCE,"TONE":TONE,"LENGTH":LENGTH,"CTA_TYPE":CTA_TYPE,"PREVIOUS_POSTS_HISTORY":PREVIOUS_POSTS_HISTORY}
        model = load_model()
        prompt = content_prompt() 
        chain = (
        prompt 
        | model 
        | (lambda message:message.content))
        response = await chain.ainvoke(param)
        State['content']=response
        return response
    @tool
    async def rewritten_content(INPUT_CONTENT:str):
            '''These tool used to improve the content quality'''
            TOPIC = State['user_query']
            AUDIENCE = Audience
            TONE = tone
            LENGTH = Length
            CTA_TYPE = CTA_type
            PREVIOUS_POSTS_HISTORY=State['chat_history']
            param = {"TOPIC":TOPIC,"AUDIENCE":AUDIENCE,"TONE":TONE,"LENGTH":LENGTH,"CTA_TYPE":CTA_TYPE,"PREVIOUS_POSTS_HISTORY":PREVIOUS_POSTS_HISTORY,"INPUT_CONTENT":INPUT_CONTENT}
            model = load_model()
            rewritten_prmpt = rewritten_prompt()
            chain = (
                rewritten_prmpt 
                | model 
                | (lambda message : message.content)) 
            State['content'] = await chain.ainvoke(param)
            return State['content']
    @tool
    async def content_quality(INPUT_CONTENT:str):
            '''These tool used to checks the quality of the content and returns quality as LOW,MEDIUM,HIGH'''
            TOPIC=State['user_query']
            model = load_model()
            prmpt = is_cntent_quality()
            chain = prmpt | model | (lambda message : message.content)
            response =await chain.ainvoke({"TOPIC":TOPIC,"INPUT_CONTENT":INPUT_CONTENT})
            quality = response.split('\n',maxsplit=1)[0].split(' ')[1]   
            State['content_quality'] = quality
            return quality
    
    model = load_model()
    tools = [create_content,rewritten_content,content_quality]
    agent_prmpt = agent_prompt()
    react_agent = create_react_agent(llm=model,tools=tools,prompt=agent_prmpt)
    agent_exec = AgentExecutor(agent=react_agent,tools=tools,verbose=True,handle_parsing_errors=True)
    response = await agent_exec.ainvoke(State)
    return response

