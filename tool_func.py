class State(TypedDict):
    user_query : Annotated[str,Field('user input to create the linkedin content')]
    chat_history: Annotated[list[BaseMessage],Field('Previous chat history data')]
    content : Annotated[str,Field('Content generated by llm')]
    content_quality : Annotated[str,Field('It returns the quality of the content')]
    

async def load_chat_history(file_path):
    if os.path.exists:
        with open(file_path,'r') as file:
            history = json.load(file)
            return history
    else:
        with open(file_path,'w+') as file:
            history = json.dump([],file)
            return history

